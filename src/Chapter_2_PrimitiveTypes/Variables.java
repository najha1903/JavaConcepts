package Chapter_2_PrimitiveTypes;

//Variables are a way to store information in our computer. Variables that we define in a program can be accessed by a name we give them

//A variable, as the name suggests can be changed, i.e it's contents are variable.

//There are lots of different types of data we can define for our variables, which are known as data types.

//Data types are keywords in Java.

//To define a variable, we need to specify the data type, then give name to the variable, and optionally assign an expression to initialise the variable with value.

//Any sequence of characters surrounded by double quotes is a String literal in Java. It's value cannot be changed,unlike a variable.

//Java operators perform an operation on a variable or value.

public class Variables {

    public static void main(String[] args) {

        int myFirstNumber = 5;  //declaration statement is used to define a variable by indicating the data type, and the name, and optionally to set the variable to a certain value.

        // Datatype = int, variable name = myFirstNumber, value = 5;

        System.out.println(myFirstNumber);
    }
}
